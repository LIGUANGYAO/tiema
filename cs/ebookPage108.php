		<p class="title2">受众的四大需求</p>

<p>如果留心，可以发现，软文还可以有无数种写法。
</p><p>本书接下来的章节，会有关于公众号标题写法的内容，其实也是在提供写软文的技巧。也建议大家去读一些广告文案写作的书籍，经常加以训练。
</p><p>不过总的来说，要想写好软文，最核心的部分是，要了解受众需求，从受众需求出发写文案，而非一味地推销自己。
</p><p>从“你”（受众）出发而非从“我”（推销者）出发，是一切营销活动的原则。微信营销也是如此。不管你写公众号内容，还是写朋友圈内容，都应该遵循这一原则。
</p><p>不同的受众有不同的需求，你需要对你的目标受众群做调研，知道他们的年龄、地域、性别、职业、收入、购买习惯等，这样才能有的放矢，做好营销。
</p><p>总的来说，受众需求可以大致归纳为四类：让我笑（有趣）、让我哭（感动）、让我热血沸腾（励志）、帮我解决问题（实用）。你写了一个软文，只要能符合有趣、感动、励志、实用中的任何一项，就算是成功了。
</p><p>当然也可以同时符合多项，既有趣，又励志；既感动，又实用。这就更高明了。
</p><p>请看减脂神奇降体服的文案：<br>“朋友们问我减肥良方，特发真心贴祝姑娘们身体健康。<br>1.您别躲家拉肚子了，是药三分毒。<br>2.减肥要吃碳水化合物，俗称主食。减脂不是挨饿，你饿死脂肪都未必会死。<br>3.运动是正道儿，快上道儿吧。<br>4.推荐减脂神奇降体服，穿着运动出汗又减脂，事半功倍！”</p>

